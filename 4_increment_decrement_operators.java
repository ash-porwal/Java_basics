class increment_decrement_operators {
    // just type main to write this whole thing public static void main(String[] args)
    public static void main(String[] args) {
        byte i = 6;
        // just type sout and hit enter to write this thing System.out.println();
        System.out.println(i++); //yaha par i ki value ke baad ++ hai..aur ye i++ print statement mein hai ..it means i ki value yaha print hogi jo upar se aa rahi value.. aur fir ++ usko increase kar dega by 1 ..for example here in above i = 6 so now it will first print that i value and then increase it by 1 so from next line we have i = 7
        System.out.println(i);
        
        // in case of ++i it will first increase then print
        System.out.println(++i);

        // it will even increase character
        char c = 'x';
        System.out.println(++c);
        
    }
}
